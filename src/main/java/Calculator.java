public class Calculator {


    public Calculator(){

    }

    public int getAdd(int num1, int num2) {
        return num1+num2;
    }

    public int getSubtract(int num1, int num2) {
        return num1-num2;
    }

    public int getMultiply(int num1, int num2) {
        return num1*num2;
    }

    public int getDivide(int num1, int num2) {
        return num1/num2;
    }
}
